const uint audioPin = 12;
PSG* psg0;

static repeating_timer_t timer;
bool ay_callback(repeating_timer_t *rt) {

   if (song_pointer == sizeof(song)/sizeof(SONG))
      song_pointer = 0;

   if (++cnt == FRAME_LOOPS_CNT) {
      while ((song[++song_pointer].reg != 0xFF) && (song_pointer<sizeof(song)/sizeof(SONG)))
         PSG_writeReg(psg0, song[song_pointer].reg, song[song_pointer].val);
      cnt = 0;
   }

   PSG_calc(psg0);

   uint16_t leftAudio = abs((int32_t)psg0->ch_out[0] + (int32_t)psg0->ch_out[1] + (int32_t)psg0->ch_out[2]) * PWM_WRAP >> 14;

   pwm_set_gpio_level(audioPin, psg0->ch_out[1]);

   return true;
}

int main(void) {

   vreg_set_voltage(VREG_VOLTAGE_1_15);
   sleep_ms(200);
   set_sys_clock_khz(250000, true);

   stdio_init_all();

   gpio_init(audioPin);
   gpio_set_dir(audioPin, GPIO_OUT);
   gpio_set_function(audioPin, GPIO_FUNC_PWM);

   uint audioSlice = pwm_gpio_to_slice_num(audioPin);

   pwm_config cfg = pwm_get_default_config();
   pwm_config_set_clkdiv(&cfg, 1.0f);
   pwm_config_set_wrap(&cfg, PWM_WRAP);
   pwm_init(audioSlice, &cfg, true);

   psg0 = PSG_new(2000000, SAMPLING_FREQ);
   PSG_setVolumeMode(psg0, EMU2149_VOL_AY_3_8910);

   add_repeating_timer_us(-(1.0/SAMPLING_FREQ) * 1000000, ay_callback, NULL, &timer);

   while(true) {

      printf("test\n");
      sleep_ms(250);
   }

    return 0;
}
